    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        StudentEntry student = students.get(dropStudentComboBox.getSelectedIndex());
        ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduleByStudent(currentSemester, student.getStudentID());
        
        String studentID = student.getStudentID();
        String firstName = student.getFirstName();
        String lastName = student.getLastName();
        StudentQueries.dropStudent(studentID);
        //Updates waitlisted student to non-waitlisted if exists
        for (ScheduleEntry schedule:schedules) {
            if (ScheduleQueries.getScheduledStudentCount(currentSemester, schedule.getCourseID()) < CourseQueries.getCourseSeats(currentSemester, schedule.getCourseID()) )
                {
                    if (schedule.getStatus().equals("S"))
                    {
                        StudentQueries.updateWaitlist();
                    }
                }
            }
        rebuildStudentComboBoxes();
        dropStudentTextArea.setText(firstName + " " + lastName + " has been dropped from the roster.");
    }//GEN-LAST:event_dropStudentButtonActionPerformed


    private void scheduleCoursesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCoursesButtonActionPerformed
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        StudentEntry student = students.get(scheduleStudentComboBox.getSelectedIndex());
        //issue is getting a studentID string neededz
        String studentID = student.getStudentID();
        String courseID = scheduleCourseComboBox.getSelectedItem().toString();
        String studentName = student.getFirstName() + " " + student.getLastName();
        // Setting up the if statement for waitlisting
        java.sql.Timestamp time = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        if (ScheduleQueries.getScheduledStudentCount(currentSemester, courseID) < CourseQueries.getCourseSeats(currentSemester, courseID))
        {
            ScheduleEntry schedule = new ScheduleEntry(courseID, currentSemester, studentID, "S", time);
            ScheduleQueries.addScheduleEntry(schedule);
            scheduleCoursesLabel.setText("Student " + studentName + " has been added to " + courseID);

        }
        else
        {
            ScheduleEntry schedule = new ScheduleEntry(courseID, currentSemester, studentID, "W", time);
            ScheduleQueries.addScheduleEntry(schedule);
            scheduleCoursesLabel.setText("Student " + studentName + " has been added to " + courseID);
        }
    }//GEN-LAST:event_scheduleCoursesButtonActionPerformed

